<template>
    <div class="card-container">
        <header class="card-header">
            <h5 class="card-title">{{card.name}}</h5>
            <div class="dropdown manage-card-container">
                <button class="btn-sm btn-dropdown btn-dropdown-task" type="button" id="dropdownMenuButton" data-toggle="dropdown" aria-haspopup="true" aria-expanded="false">
                    <img src="../../img/more-options-black.svg" alt="multiple-choices"/>
                </button>
                <div class="dropdown-menu">
                    <button class="dropdown-item btn-delete" @click="deleteCard" :data-card="card.id" :data-category="card.category_id">Delete</button>
                    <div class="dropdown-divider"></div>
                    <a class="dropdown-item" href="#">Couleur</a>
                </div>
            </div>
        </header>
        <div class="card-body">
            <task-component v-bind:card="card"></task-component>
        </div>
        <div class="form-add-task-container">
            <button class="btn btn-add-task" @click="toggleFormTask" type="button" data-target="#add-card-modal" v-if="!isVisible">+ TÃ¢che</button>
            <form class="form-add-task" v-if="isVisible" @submit="addTask" :data-card="card.id" :data-category="card.category_id">
                <div class="form-add-task-input-container">
                    <input autocomplete="off" class="form-add-task-input" type="text" name="task" id="task" v-bind:value="name">
                </div>
                <div class="buttons-add-task">
                    <button class="btn cancel" type="button" data-target="#add-card-modal" @click="toggleFormTask">Annuler</button>
                    <input class="btn submit"type="submit">
                </div>
            </form>
        </div>
    </div>
</template>

<script>
    import TaskComponent from "./TaskComponent";
    export default {
        name: "CardComponent",
        components: {TaskComponent},
        props: ['card'],
        data() {
            return {
                isVisible: false,
                name: ''
            }
        },
        methods:{
            deleteCard: function (e) {
                this.$store.dispatch('deleteCard', {cardId: e.target.dataset.card, categoryId: e.target.dataset.category})
            },
            toggleFormTask: function () {
                if (this.isVisible) {
                    this.isVisible = false
                } else {
                    this.isVisible = true
                }
            },
            addTask: function (e) {
                e.preventDefault()
                this.$store.dispatch('insertTask', {
                    taskName: e.target.task.value,
                    categoryId: e.target.dataset.category,
                    cardId: e.target.dataset.card
                })
            }
        }
    }
</script>
